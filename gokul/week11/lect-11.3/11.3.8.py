# What is enumerate in Python?
# The enumerate() function in Python is a built-in function that adds a counter to an iterable and returns it as an enumerate object.
#  This allows you to loop through the items in the iterable while keeping track of the index of each item.


# In simple terms, enumerate() takes an iterable (like a list, tuple, or string) and returns an iterator that produces pairs of index and value for each item in the iterable.
