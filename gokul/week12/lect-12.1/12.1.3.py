 # Did you noticed something between map() and reduce() function

# They are both higher-order functions that operate on iterables, but they serve different purposes.
# map() applies a given function to each item of an iterable and returns a new iterable with the results.
#  It is used for transforming data.

# reduce() applies a given function cumulatively to the items of an iterable,
#  from left to right, reducing the iterable to a single value. It is used for aggregating data.